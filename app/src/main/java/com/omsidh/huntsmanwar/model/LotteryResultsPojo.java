package com.omsidh.huntsmanwar.model;

public class LotteryResultsPojo {
    private String id, title, time, fee, prize, total_joined, my_number, rules, image, name, lottery_no;

    public LotteryResultsPojo() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getFee() {
        return fee;
    }

    public void setFee(String fee) {
        this.fee = fee;
    }

    public String getPrize() {
        return prize;
    }

    public void setPrize(String prize) {
        this.prize = prize;
    }

    public String getTotal_joined() {
        return total_joined;
    }

    public void setTotal_joined(String total_joined) {
        this.total_joined = total_joined;
    }

    public String getMy_number() {
        return my_number;
    }

    public void setMy_number(String my_number) {
        this.my_number = my_number;
    }

    public String getRules() {
        return rules;
    }

    public void setRules(String rules) {
        this.rules = rules;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLottery_no() {
        return lottery_no;
    }

    public void setLottery_no(String lottery_no) {
        this.lottery_no = lottery_no;
    }
}
